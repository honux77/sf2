    # Case 3: <img src="pic/08.GIF"> -> <img src="file/pic/08.GIF">
    content = re.sub(r'<img src="pic/([^"]+)">', r'<img src="file/pic/\1">', content, flags=re.IGNORECASE)

    with open(output_file, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f"Successfully translated and saved to {output_file}")

